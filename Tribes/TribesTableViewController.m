#pragma mark - User login/signup

-(void)signUp {
    
    // sign up user anonymously (no username/password)
    [PFAnonymousUtils logInWithBlock:^(PFUser *user, NSError *error) {
        if (error) {
            NSLog(@"Anonymous login failed.");
        } else {
            NSLog(@"Anonymous user logged in.");
            
            //set user
            currentUser = user;
        }
    }];
}
